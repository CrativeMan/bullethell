package io.crative.bullethell.entities;

/**
 * Entity
 */
public class Entity {
    protected int xPos, yPos, width, height;
    
    public Entity(int x, int y, int width, int height){
        this.xPos = x;
        this.yPos = y;
        this.width = width;
        this.height = height;
    }
}
        
